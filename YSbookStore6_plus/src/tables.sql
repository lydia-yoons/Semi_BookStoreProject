CREATE TABLE BOOK --책테이블
(
 BOOKSEQ VARCHAR2(100) PRIMARY KEY,
BOOKNAME VARCHAR2(90),
BOOKAUTHOR VARCHAR2(60),
BOOKHOUSE VARCHAR2(60),
BOOKPRICE VARCHAR2(30),
BOOKCOUNT NUMBER
);
create sequence BOOKSEQ; --시퀀스


CREATE TABLE USERS -- 유저테이블
(
 USERID VARCHAR2(30) PRIMARY KEY,
USERPW VARCHAR2(30), 
username VARCHAR2(30),
 userphone VARCHAR2(60)
);



CREATE TABLE BUY --구매 테이블
(
BUYSEQ VARCHAR2(100) PRIMARY KEY,
USERID VARCHAR2(30),
BOOKSEQ VARCHAR2(100),
FOREIGN KEY (USERID) REFERENCES USERS(USERID),
FOREIGN KEY (BOOKSEQ) REFERENCES BOOK(BOOKSEQ)
);
create sequence buyseq; --시퀀스